import React, { useEffect, useState } from "react";
import { Admin, DataProvider, Resource } from "react-admin";
import buildGraphQLProvider from "./data-provider/graphqlDataProvider";
import { theme } from "./theme/theme";
import Login from "./Login";
import "./App.scss";
import Dashboard from "./pages/Dashboard";
import { TestEntityList } from "./testEntity/TestEntityList";
import { TestEntityCreate } from "./testEntity/TestEntityCreate";
import { TestEntityEdit } from "./testEntity/TestEntityEdit";
import { TestEntityShow } from "./testEntity/TestEntityShow";
import { jwtAuthProvider } from "./auth-provider/ra-auth-jwt";

const App = (): React.ReactElement => {
  const [dataProvider, setDataProvider] = useState<DataProvider | null>(null);
  useEffect(() => {
    buildGraphQLProvider
      .then((provider: any) => {
        setDataProvider(() => provider);
      })
      .catch((error: any) => {
        console.log(error);
      });
  }, []);
  if (!dataProvider) {
    return <div>Loading</div>;
  }
  return (
    <div className="App">
      <Admin
        title={"mend admin tool-1"}
        dataProvider={dataProvider}
        authProvider={jwtAuthProvider}
        theme={theme}
        dashboard={Dashboard}
        loginPage={Login}
      >
        <Resource
          name="TestEntity"
          list={TestEntityList}
          edit={TestEntityEdit}
          create={TestEntityCreate}
          show={TestEntityShow}
        />
      </Admin>
    </div>
  );
};

export default App;
